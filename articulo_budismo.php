            <p>Siddharta Gautama, uno de los Budhas de una larga línea según la tradición, a raíz de lo que observó como el enquistamieno de la religión Brahamanica en una proliferación del culto religioso en miles de divinidades, decidió hacer una reforma cultural con la finalidad de permitir un avance evolutivo de su grupo cultural. Para ello creo lo que fue una Psicología Espiritual. La llamamos así porque, ¿Qué es sino eso el Hinayana?. El Buda es también llamado el Sr. del Karma, el que hace girar la rueda del Karma porque es un guía espiritual de la humanidad que lleva con su doctrina a la humanidad a un estadío superior de consciencia.
            Los postulados básicos del Budismo reconocidos por ambas corrientes y todas sus posteriores ramas son las Cuatro Nobles Verdades y el Noble Óctuple Sendero.</p>
