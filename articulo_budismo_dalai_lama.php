          <p>Sabemos que el enorme progreso material no ha podido por sí mismo darnos la felicidad que todos queremos. Su Santidad el Dalai Lama enseña que es necesario desarrollar cualidades humanas fundamentales en nuestro interior, como el amor, la compasión, la empatía, la paciencia, el contentamiento. Estos valores irán transformando nuestra actitud y nos ayudarán a encontrar más paz y bienestar incondicional.</p>
          <p>“Las emociones distorsivas son estados de la mente que, cuando se manifiestan en el continuo de nuestros pensamientos, nos dejan perturbados, confundidos e infelices. Por lo tanto, esos estados de la mente que nos afligen o engañan son llamados distorsiones o emociones aflictivas”. “Monjes, hay seres que pueden no sufrir enfermedades físicas durante períodos de uno, dos e incluso cien años. Pero es muy difícil encontrar en el mundo, seres que puedan admitir que se encuentran libres de perturbaciones mentales en algún momento, con la única excepción de aquellos que han destruido las distorsiones.” Buda [Anguttara Nikaya (A.II:143); Samyutta Nikaya (S.III.:2)]</p>
          <p>La Psicología Budista describe cuatro agregados mentales: Sentimiento, Discriminación, Consciencia Primaria (los cinco sentidos y la atención consciente de la mente) y otros aspectos, reunidos dentro de los Factores Composicionales. </p>
          <p>El Sentimiento Agregado es definido como un factor omnipresente de la mente que clasifica las experiencias en tres categorías, a saber: agradables, desagradables o neutras:</p>
          <p>Cuando categorizamos un objeto como placentero (agradable), desarrollamos el apego. Cuando categorizamos un objeto como displacentero (desagradable), desarrollamos aversión e incluso enojo u odio. Cuando categorizamos un objeto como neutral, a menudo no nos preocupamos por ese objeto y hasta podemos llegar a ignorarlo.</p>
          <p>El proceso de etiquetamiento del agregado del sentimiento usualmente dura una fracción de segundo. Luego de aplicar una etiqueta sobre un objeto, tendemos a crear una opinión e imagen estática del objeto en nuestra mente. En este estadío, usualmente, la semilla del prejuicio es sembrada. Una vez que hemos establecido la opinión de que algo es placentero o displacentero, generalmente, necesitamos una gran cantidad de evidencia antes de estar dispuestos a cambiar de parecer -si es que siquiera estamos preparados a hacerlo-. Una vez que hemos categorizado a un objeto como displacentero o malo, ese objeto aparece como si fuese malo en sí mismo, como si la maldad fuese una cualidad inherente.</p>
          <p>Podemos etiquetar a una persona como “mala”, pero seguramente ¡sus amigos no estén de acuerdo! Por lo tanto, necesitaremos darnos cuenta de que “bueno” y “malo” son meras opiniones, y que la opinión usualmente se funda en nada más que un primer vistazo y casi un etiquetamiento inmediato. Las cosas y las personas cambian más rápido que nuestras etiquetas. Todos tienden al prejuicio. El etiquetamiento es una manera conveniente de rápidamente darle algún sentido a nuestro mundo circundante, mediante la categorización de las cosas en términos de “bueno” o “malo”. El principal problema es que tendemos a reaccionar frente al mundo a partir de meras (super)simplificadas etiquetas.</p>
          <p>Nuestra mente no mira en forma natural dentro de sí, no se mira a sí misma. Produce opiniones, emite juicios, etc., pero siempre está afuera. Entonces, para que la mente se mire a sí misma necesitamos la ayuda de un conocimiento que nos oriente en ese sentido. La filosofía y psicología budista desde hace 2500 años que indica ese rumbo.</p>
