          <p>Para entender el Zen podemos citar a un Maestro Zen: "Para un hombre, antes de estudiar Zen, las montañas son montañas y aguas, aguas Pero cuando ve la verdad del Zen a través de la instrucción de un buen maestro, las montañas y las aguas ya lo que no son. son, sin embargo, cuando más tarde en realidad llegó al lugar de descanso (el estado de Satori), las montañas y las aguas vuelven a ser lo que eran". </p>
          <p>Zen es una psicología no en el sentido occidental del término, sino más bien como la experiencia de la realidad última de toda la existencia y en última instancia, la mente misma, que es uno con toda la humanidad, el inconsciente y arquetipos colectivos, que se llama No-Mente ( racionalista Ego) por el Zen.</p>
          <p>Cuando se trata de definir qué es el Zen es, nos encontramos con la dificultad de acercarse a una mentalidad completamente diferente. Usted necesita saber que el Zen es una de las muchas ramas del tronco común que es el budismo original de la India que data de el siglo V aC y que se conoce con el nombre de Hinayana (pequeño vehículo). La base filosófica de la que se basa, y según la cual cualquier monje zen es entrenado es la doctrina de DharmaDathu. Dharma se traduce como -Método-Doctrina - Operación y Dathu como España o del Mundo.</p>
          <p>El experto Zen y practicante mencionó anteriormente, Alan Watts, traduce estos dos términos como la Doctrina de la Operación del Universo. El concepto fundamental contenida en el mismo es que todo lo que existe es relativo a todo lo demás y que tres distorsiones o errores de la mente racional (racionalista) del Ego que impiden la experiencia de lo que es real y estos son:
          </p>
          <p>1) Las cosas, la concepción del universo como cosas aisladas unas de otras.</p>
          <p>2) Agentes, la noción de un sujeto separado o Ego que realiza la acción.</p>
          <p>3) La recompensa y el castigo, un claro ejemplo de esto se encuentra en la tradición judeo - la doctrina cristiana.</p>
          <p>También encontramos en la idea errónea del karma exportado a Occidente. El monje Zen a través de una disciplina monástica rigurosa, cuyo objetivo es centrar toda la atención en el mundo interior o psicológico, busca lograr la experiencia de Satori (conciencia acoplado o integrado a la Ser, como Jung lo llamó), para experimentar lo que está más allá del 3 distorsiones citados antes.
          </p>
          <p>El monje trabaja directamente con el Inconsciente Colectivo y partiendo de la premisa de su existencia. Parte de su práctica es aceptar la guía de una conciencia superior consagrado en la Roshi (Maestro Zen) además de ver en ella un reflejo de su propia naturaleza profunda, Autoservicio de Jung o Atman del hindú.</p>
          <p>Como Jung señaló claramente en su introducción de la parte oriental de la mente occidental, los europeos y sus descendientes americanos, debemos primero tener un método psicológico para proporcionarnos el conocimiento estricto de lo que la mente es, dotado de estructura y en el tiempo de una visión materialista no del mundo y el hombre, es decir, una psicología completa y no reduccionista como teorías materialistas modernos de Freud y el conductismo son.</p>
          <p>La mente oriental hasta su colonización por parte de Occidente no tenía que superar las trampas del materialismo porque se arraiga en una experiencia mística de la existencia. En Occidente tenemos que utilizar los métodos que ofrece el Evolutiva Psychologies. De lo contrario, nos acercamos a la espiritualidad llena de ideas fantasiosas expresadas en la búsqueda de los poderes espirituales que han tenido tanta prensa y difusión de series de televisión, películas y libros de los más vendidos de acercamiento superficial a los antiguos métodos de auto -Transformación del Este.</p>
          <p>Algunos de los métodos utilizados por el Zen a la consecución de una mayor conciencia o Satori son: Koan, clase de paradojas que atraen al emerger de la intuición, para llegar a la experiencia respuesta correcta (entendida en términos de Jung, como la inteligencia de el inconsciente colectivo); Zazen, la meditación sentada; la ceremonia del té, el arte del tiro con arco y el Zen Garden.</p>
