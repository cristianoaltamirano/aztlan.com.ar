            <p class="lead">Vamos a dar una aproximación a la psicología del budismo zen y para ello vamos a utilizar las principales ideas del psicólogo y maestro Carl G. Jung porque él es una autoridad en el tema. Uno de los exponentes más importantes del Zen en Occidente fue el monje y maestro de DT Suzuki (1870-1966) y su secretario y discípulo Alan Watts. Jung trabajó con Suzuki en la introducción del Zen a Occidente en el famoso libro Introducción al budismo zen.</p>
