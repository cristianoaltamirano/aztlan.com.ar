          <p>Esta amalgama contrasta con la superespecialización del conocimiento y de las profesiones que se dio en los últimos siglos en Occidente; al punto de que llegamos a preguntarnos “¿qué tendrá que ver la Psicología con la Filosofía?” Olvidando que la psicología ya existía también en la antigua Grecia dentro de la Filosofía, (ver un ejemplo de esto en el tratado de Teofrasto sobre los tipos psicológicos, S.IV a. de C.). Por otro lado, la influencia de Occidente sobre Oriente, de los últimos dos siglos que contribuyó a que muchas de estas tradiciones culturales y religiosas se hayan ido perdiendo, con la irrupción del modo de vida “moderno”. </p>
          <p>La Psicología ocupa un lugar destacado en la historia cultural de la India. Ya en el periodo de los Upanishad, es decir aproximadamente en 1500 a.C, se enseñó que la psique y el cosmos se encuentran inseparablemente relacionados. Separado de la psique, el Universo es una incógnita. Separada del universo, la psique es una abstracción desarraigada. Ir de lo Universal a lo particular, es característica de la  Filosofía de la India,  es decir el Ser humano en el Universo, y su relación con él. Llamaron Atman a la Conciencia o el Yo real en contraposición con el yo-ego con minúscula que es sólo un reflejo o satélite del verdadero Yo o Yo Real. </p>
          <p>En Occidente se piensa que el Yoga son fundamentalmente las asanas o posturas físicas. Poco se sabe de la filosofía de aquello que lleva el mismo nombre. La palabra Yoga quiere decir unión, unión con lo superior dentro de uno mismo. Para lograr esta unión en la India existen desde hace milenios seis escuelas de filosofía cada una con una cosmovisión y prácticas particulares orientadas a que el alumno pueda lograr un estado superior de consciencia.</p>
          <p>Todas las escuelas filosóficas y cultos religiosos de la India surgen de un tronco en común que son los Vedas. Los Vedas son cuatro libros sagrados de los cuales nos ha llegado la versión de su recopilador Vhyasa, sabio y personaje mítico del Hinduismo. Por la complejidad de los Vedas se dieron enseñanzas explicativas de estos en los Brahmanas, luego en los Puranas y finalmente a una síntesis en los Upanishad. </p>
          <p>Los Upanishad, 108 en total, representan la culminación lógica y la porción más filosófica de los Veda. Los Vedas y los Upanishad se consideran entre las antiguas escrituras de la India, las más sagradas y cargadas de conocimiento. Desde el Siglo 5 a  de C. hasta el Siglo XVII, como resultado de una intensa sistematización del pensamiento y codificación de los principios de la Filosofía Yoga, se constituyeron varias disciplinas yogas bien definidas. Algunas de ellas son: El Yoga del control de la respiración y el cuerpo: Hatha Yoga. El Yoga devocional o del amor: Bhakti; El yoga del control de la mente: Raja Yoga. El Yoga del Conocimiento: Jñana. El Yoga de la Acción: Karma.</p>
          <p>La meta última de todas las mencionadas es la unión con el Yo Real o Atman. Difieren entre ellas sólo en el punto de partida y en el método; cada una de ellas pone de relieve uno de los varios ingredientes de la personalidad, pero todas trabajan al unísono.
          Dice el Hinduismo que la reflexión que produce la me-ditación conduce a un más profundo autoconocimiento, la mente (citta) se esclarece con la más profunda verdad: el auténtico yo en uno mismo (atman, purusha) es la llave maestra para dejar lograr el cambio interior, llegar a la estructura esencial del universo y al misterio del ser. De este modo, los antiguos sabios de la India estaban convencidos de que la búsqueda de la suprema verdad o del misterio del universo debe asumir la búsqueda del verdadero yo de uno mismo. El proceso de auto indagación psicológica, intrépida aventura, fue el fundamento de la filosofía yoga. </p>
          <p>En síntesis, el camino yoga consiste en diversas autodisciplinas ético-religiosas, psico-vitales y psico-espirituales que se deben seguir con el objeto de alcanzar la meta última de la auto-realización. </p>
          <p>La distinción entre mente (citta) y el sí-mismo (purusha, atman) es de fundamental importancia en la psicología yoga. Una integración de los componentes de la psique es esencial para la autorrealización. </p>
