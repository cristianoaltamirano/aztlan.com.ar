            <p>Retomaremos ahora a las dos cosas que mencioné —a saber—, que nuestro Ser atrae nuestra vida y no nos conocemos a nosotros mismos. —¿Qué es lo que impide que esto suceda así? Las respuestas son sencillas. Las ilusiones lo impiden. En el primer caso, no somos culpables de nada de lo que nos sucede en el sentido ordinario. Es siempre la culpa del otro. Es así como lo creemos mecánicamente. Por eso no podemos ver que la culpa es verdaderamente nuestra. Por lo tanto no deseamos cambiar nuestro Ser ni vemos conexión alguna entre esto y lo que nos sucede en la vida. De hecho, me imagino que en realidad no captamos tener un Ser de una forma particular. En el segundo caso, desde luego nos conocemos a nosotros mismos. ¡Qué tontería decir que no me conozco a mí mismo. ¿Quién podría conocerme mejor que yo mismo? Esto es de nuevo pura ilusión. Hay maneras de librarse de esto, y siempre las hubo, en todas las épocas. Porque la enseñanza esotérica se refiere a salir de ello.</p>
