            <p>La carencia de conocimiento de sí es la regla en la existencia humana y contribuye en mucho al inútil sufrimiento del Hombre. Porque la energía humana que se gasta en sufrimiento inútil, cuando se la ve en una visión, es increíble y terrible. ¿Ha notado usted, de paso, cuánta energía derrocha realmente en sufrimiento inútil? Tal vez haya pasado la mañana sintiéndose desdichado, o de mal humor, o lleno de auto-compasión- Pues bien, ese es sufrimiento inútil. Hemos de sacrificar nuestro sufrimiento, es decir prescindir del sufrimiento inútil, quitarlo de nuestras vidas como quien quita una maleza de su jardín —esta es una idea presenta también en el budismo, además del cuarto camino y también de la psicología de Jung-.
